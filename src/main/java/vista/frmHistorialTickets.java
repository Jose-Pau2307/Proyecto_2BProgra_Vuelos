    public void cargarListaTickets() {
    DefaultListModel<String> modeloLista = new DefaultListModel<>();
    File carpetaProyecto = new File("."); 
    
    // Filtro para archivos de ticket
    File[] archivos = carpetaProyecto.listFiles((dir, nombre) -> 
        nombre.startsWith("Ticket_") && nombre.endsWith(".txt")
    );

    if (archivos != null && archivos.length > 0) {
        for (File archivo : archivos) {
            modeloLista.addElement(archivo.getName());
        }
    } else {
        modeloLista.addElement("No hay tickets generados");
    }
    listArchivos.setModel(modeloLista);
}

