    private void listArchivosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listArchivosValueChanged
        // Evita que el código se ejecute dos veces (al presionar y al soltar)
    if (!evt.getValueIsAdjusting()) {
        String archivoSeleccionado = listArchivos.getSelectedValue();
        
        if (archivoSeleccionado != null && !archivoSeleccionado.equals("No hay tickets generados")) {
            try {
                // Usamos File para asegurarnos de que la ruta es relativa a la raíz
                File archivo = new File(archivoSeleccionado);
                
                // Leemos todas las líneas y las unimos con saltos de línea
                String contenido = new String(java.nio.file.Files.readAllBytes(archivo.toPath()));
                
                txtContenidoTicket.setText(contenido);
                txtContenidoTicket.setCaretPosition(0); // Scroll al inicio
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al leer el archivo: " + ex.getMessage());
            }
        }
    }
    }//GEN-LAST:event_listArchivosValueChanged

